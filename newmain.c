// ???????????????????????????????????????????????????????????????????????????????
// INTERRUPCIÓN ? Timer0
// Genera flag cada ~500 ms con Fosc=4MHz, prescaler 1:256
//
// Cada overflow de TMR0 (256 ciclos × 4 = 1024 µs con prescaler 256 = ~0.98 ms
// Recargamos TMR0=6 ? 250 ciclos × 4µs × 256 = ~256 ms... ajustamos:
// Para ~10 ms por tick: TMR0 reload = 256 - (10ms / (4µs × 256)) = 256-10 = 246
// 50 ticks × 10ms = 500ms
// ???????????????????????????????????????????????????????????????????????????????
void __interrupt() isr(void) {
    if (TMR0IF) {
        TMR0IF = 0;
        TMR0   = 246;           // Recargar para ~10 ms
        ticks_timer++;
        if (ticks_timer >= 50) {
            ticks_timer  = 0;
            flag_timer   = 1;   // Marcar que pasaron ~500 ms
        }
    }
}

// ???????????????????????????????????????????????????????????????????????????????
// CONFIGURACIÓN DEL TIMER0
// ???????????????????????????????????????????????????????????????????????????????
void configurar_timer0(void) {
    OPTION_REGbits.T0CS  = 0;  // Clock interno (Fosc/4)
    OPTION_REGbits.PSA   = 0;  // Prescaler asignado a Timer0
    OPTION_REGbits.PS    = 0b111; // Prescaler 1:256
    TMR0   = 246;
    TMR0IF = 0;
    TMR0IE = 1;                // Habilitar interrupción Timer0
    GIE    = 1;                // Habilitar interrupciones globales
    PEIE   = 1;
}

// ???????????????????????????????????????????????????????????????????????????????
// LECTURA EEPROM INTERNA
// ???????????????????????????????????????????????????????????????????????????????
uint8_t leer_EEPROM(uint8_t address) {
    EEADR  = address;
    EECON1bits.EEPGD = 0;  // Acceso a datos EEPROM (no a Flash)
    EECON1bits.RD    = 1;  // Iniciar lectura
    return EEDATA;
}

// ???????????????????????????????????????????????????????????????????????????????
// ESCRITURA EEPROM INTERNA
// ???????????????????????????????????????????????????????????????????????????????
void escribir_EEPROM(uint8_t address, uint8_t data) {
    uint8_t gi_state = GIE;
    EEADR  = address;
    EEDATA = data;
    EECON1bits.EEPGD = 0;  // Datos EEPROM
    EECON1bits.WREN  = 1;  // Habilitar escritura
    GIE = 0;               // Deshabilitar interrupciones durante secuencia
    EECON2 = 0x55;         // Secuencia de escritura obligatoria
    EECON2 = 0xAA;
    EECON1bits.WR = 1;     // Iniciar escritura
    while (EECON1bits.WR); // Esperar a que termine
    EECON1bits.WREN = 0;   // Deshabilitar escritura
    GIE = gi_state;        // Restaurar estado de interrupciones
}

// ???????????????????????????????????????????????????????????????????????????????
// CARGAR UMBRALES DESDE EEPROM (al arranque)
// ???????????????????????????????????????????????????????????????????????????????
void cargar_umbrales_EEPROM(void) {
    uint8_t magic = leer_EEPROM(EEPROM_MAGIC);
    if (magic == EEPROM_MAGIC_VAL) {
        // Datos válidos guardados previamente
        t_von  = (int8_t)leer_EEPROM(EEPROM_VON);
        t_voff = (int8_t)leer_EEPROM(EEPROM_VOFF);
        // Validación de integridad mínima
        if ((t_von  < T_CONFIG_MIN) || (t_von  > T_CONFIG_MAX)) t_von  = T_ON_DEFAULT;
        if ((t_voff < T_CONFIG_MIN) || (t_voff > T_CONFIG_MAX)) t_voff = T_OFF_DEFAULT;
        if ((t_von - t_voff) < DIFF_MIN) {
            t_von  = T_ON_DEFAULT;
            t_voff = T_OFF_DEFAULT;
        }
    } else {
        // Primera vez: usar valores por defecto y guardarlos
        t_von  = T_ON_DEFAULT;
        t_voff = T_OFF_DEFAULT;
        guardar_umbrales_EEPROM();
    }
}

// ???????????????????????????????????????????????????????????????????????????????
// GUARDAR UMBRALES EN EEPROM
// ???????????????????????????????????????????????????????????????????????????????
void guardar_umbrales_EEPROM(void) {
    escribir_EEPROM(EEPROM_MAGIC, EEPROM_MAGIC_VAL);
    escribir_EEPROM(EEPROM_VON,  (uint8_t)t_von);
    escribir_EEPROM(EEPROM_VOFF, (uint8_t)t_voff);
}

// ???????????????????????????????????????????????????????????????????????????????
// LECTURA ADC ? Canal AN0, 10 bits, justificado a la derecha
// ???????????????????????????????????????????????????????????????????????????????
uint16_t leer_ADC(void) {
    ADCON0bits.GO_DONE = 1;
    while (ADCON0bits.GO_DONE);
    return ((uint16_t)(ADRESH << 8) | ADRESL);
}

// ???????????????????????????????????????????????????????????????????????????????
// CONVERSIÓN ADC ? TEMPERATURA
//   También devuelve el voltaje calculado (por referencia)
//   Fórmula: Temp = T_MIN + Vout × (RANGO_T / VOUT_MAX)
// ???????????????????????????????????????????????????????????????????????????????
float convertir_temperatura(uint16_t lectura, float *voltaje_out) {
    float vout = (lectura * VCC_ADC) / ADC_RESOL;
    if (voltaje_out) *voltaje_out = vout;
    float temp = T_MIN + vout * (RANGO_T / VOUT_MAX);
    if (temp < T_MIN)              temp = T_MIN;
    if (temp > (T_MIN + RANGO_T))  temp = T_MIN + RANGO_T;
    return temp;
}

// ???????????????????????????????????????????????????????????????????????????????
// CONTROL DE HISTÉRESIS DEL VENTILADOR
//   Solo actúa en MODO_AUTO. En MODO_MANUAL lo controla el usuario.
// ???????????????????????????????????????????????????????????????????????????????
void control_histeresis(void) {
    if (modo_actual == MODO_AUTO) {
        if (temperatura_actual > (float)t_von) {
            fan_on  = 1;
            FAN_PIN = 1;
        } else if (temperatura_actual < (float)t_voff) {
            fan_on  = 0;
            FAN_PIN = 0;
        }
        // Zona intermedia: no modificar ? histéresis preservada
    }
}

// ???????????????????????????????????????????????????????????????????????????????
// CONTROL DE ALARMA DE TEMPERATURA CRÍTICA
//   Activa RD1 y bandera de parpadeo LCD cuando temp > 58°C
//   Desactiva automáticamente cuando temp < 56°C (histéresis 2°C)
// ???????????????????????????????????????????????????????????????????????????????
void control_alarma(void) {
    if (!alarma_activa) {
        if (temperatura_actual >= T_ALARMA_ON) {
            alarma_activa   = 1;
            ALARM_PIN       = 1;
        }
    } else {
        if (temperatura_actual < T_ALARMA_OFF) {
            alarma_activa   = 0;
            ALARM_PIN       = 0;
            alarma_parpadeo = 0;
        }
        // Alternar parpadeo cada ciclo (cada 500 ms)
        alarma_parpadeo = !alarma_parpadeo;
    }
}

// ???????????????????????????????????????????????????????????????????????????????
// LECTURA Y PROCESAMIENTO DE BOTONES
//
//  Anti-rebote por software: se requieren N lecturas consecutivas estables
//  (~20 ms ? con flag_timer cada 500 ms usamos contador de llamadas, pero
//   para mayor precisión del debounce se verifica en cada ciclo del loop
//   principal con __delay_ms(1) mínimo o desde ISR ? aquí se implementa
//   mediante un contador de estabilidad de N=3 lecturas en el bucle principal
//   que se llama cada ~1 ms si se usa un poll, o bien directamente desde el
//   ciclo principal sin delay. Para la pulsación larga de 2 s, se cuenta
//   cuántas veces consecutivas se lee RB0=0 en el loop principal a ~1 ms).
//
//  NOTA: Esta función debe llamarse frecuentemente (cada ~1 ms) desde main,
//        por eso NO depende de flag_timer.
// ???????????????????????????????????????????????????????????????????????????????
void leer_botones(void) {
    // ?? Leer estado raw de los tres botones (activos en bajo) ?????????????????
    uint8_t rb0 = BTN_MODE  ? 0 : 1;   // 1 = presionado
    uint8_t rb1 = BTN_UP    ? 0 : 1;
    uint8_t rb2 = BTN_DOWN  ? 0 : 1;

    // ?????????????????????????????????????????????????????????????????????????
    // BOTÓN RB0 ? MODE
    //   Pulsación corta (<2s): alterna modo / cicla vista / alterna campo
    //   Pulsación larga (?2s): entra o sale de CONFIGURACIÓN
    // ?????????????????????????????????????????????????????????????????????????
    if (rb0) {
        // Botón presionado: incrementar contador para pulsación larga
        if (rb0_debounce < 20) rb0_debounce++;   // Anti-rebote: 20 ms estable

        if (rb0_debounce >= 20) {
            rb0_hold_count++;  // Cada llamada ~1 ms ? 2000 = 2 segundos
            if ((rb0_hold_count >= 2000) && !rb0_hold_fired) {
                rb0_hold_fired = 1;
                // ?? Acción pulsación LARGA ??????????????????????????????????
                if (modo_actual != MODO_CONFIG) {
                    // Entrar a CONFIGURACIÓN
                    modo_anterior = modo_actual;
                    modo_actual   = MODO_CONFIG;
                    campo_edit    = EDIT_VON;
                    LCD_Clear();
                } else {
                    // Salir de CONFIGURACIÓN: guardar en EEPROM
                    guardar_umbrales_EEPROM();
                    modo_actual = modo_anterior;
                    LCD_Clear();
                }
            }
        }
    } else {
        // Botón liberado
        if ((rb0_debounce >= 20) && !rb0_hold_fired) {
            // ?? Acción pulsación CORTA ??????????????????????????????????????
            if (modo_actual == MODO_CONFIG) {
                // Alternar campo editado (Von ? Voff)
                campo_edit = (campo_edit == EDIT_VON) ? EDIT_VOFF : EDIT_VON;

            } else if (modo_actual == MODO_AUTO) {
                // Ciclar vista: Normal ? Max ? Min ? Normal
                vista_actual = (Vista_t)((vista_actual + 1) % 3);
                LCD_Clear();

            } else if (modo_actual == MODO_MANUAL) {
                // Volver a modo AUTO con pulsación corta
                modo_actual  = MODO_AUTO;
                vista_actual = VISTA_NORMAL;
                LCD_Clear();
            }
        }
        // Reset contadores
        rb0_debounce   = 0;
        rb0_hold_count = 0;
        rb0_hold_fired = 0;
    }
    rb0_prev = rb0;

    // ?????????????????????????????????????????????????????????????????????????
    // BOTÓN RB1 ? UP
    // ?????????????????????????????????????????????????????????????????????????
    if (rb1 && !rb1_prev) {
        // Flanco de bajada detectado (debut de pulsación)
        rb1_debounce = 0;
    }
    if (rb1) {
        if (rb1_debounce < 20) rb1_debounce++;
        if (rb1_debounce == 20) {
            // Acción confirmada (anti-rebote cumplido)
            if (modo_actual == MODO_MANUAL) {
                // Encender ventilador manualmente
                fan_on  = 1;
                FAN_PIN = 1;
                rb1_debounce = 21; // Prevenir disparos repetidos
            } else if (modo_actual == MODO_CONFIG) {
                // Subir umbral seleccionado
                if (campo_edit == EDIT_VON) {
                    int8_t nuevo_von = t_von + 1;
                    if (nuevo_von <= T_CONFIG_MAX && (nuevo_von - t_voff) >= DIFF_MIN) {
                        t_von = nuevo_von;
                        advertencia_activa = 0;
                    } else {
                        advertencia_activa = 1;
                        advertencia_count  = 0;
                    }
                } else {
                    int8_t nuevo_voff = t_voff + 1;
                    if (nuevo_voff <= T_CONFIG_MAX && (t_von - nuevo_voff) >= DIFF_MIN) {
                        t_voff = nuevo_voff;
                        advertencia_activa = 0;
                    } else {
                        advertencia_activa = 1;
                        advertencia_count  = 0;
                    }
                }
                rb1_debounce = 21;
            }
        }
    } else {
        rb1_debounce = 0;
    }
    rb1_prev = rb1;

    // ?????????????????????????????????????????????????????????????????????????
    // BOTÓN RB2 ? DOWN
    // ?????????????????????????????????????????????????????????????????????????
    if (rb2 && !rb2_prev) {
        rb2_debounce = 0;
    }
    if (rb2) {
        if (rb2_debounce < 20) rb2_debounce++;
        if (rb2_debounce == 20) {
            if (modo_actual == MODO_MANUAL) {
                // Apagar ventilador manualmente
                fan_on  = 0;
                FAN_PIN = 0;
                rb2_debounce = 21;
            } else if (modo_actual == MODO_CONFIG) {
                // Bajar umbral seleccionado
                if (campo_edit == EDIT_VON) {
                    int8_t nuevo_von = t_von - 1;
                    if (nuevo_von >= T_CONFIG_MIN && (nuevo_von - t_voff) >= DIFF_MIN) {
                        t_von = nuevo_von;
                        advertencia_activa = 0;
                    } else {
                        advertencia_activa = 1;
                        advertencia_count  = 0;
                    }
                } else {
                    int8_t nuevo_voff = t_voff - 1;
                    if (nuevo_voff >= T_CONFIG_MIN && (t_von - nuevo_voff) >= DIFF_MIN) {
                        t_voff = nuevo_voff;
                        advertencia_activa = 0;
                    } else {
                        advertencia_activa = 1;
                        advertencia_count  = 0;
                    }
                }
                rb2_debounce = 21;
            }
        }
    } else {
        rb2_debounce = 0;
    }
    rb2_prev = rb2;

    // ?????????????????????????????????????????????????????????????????????????
    // DETECCIÓN DE PULSACIÓN SIMULTÁNEA RB1+RB2 EN MODO AUTO ? entra MANUAL
    // ?????????????????????????????????????????????????????????????????????????
    if (rb1 && rb2 && (modo_actual == MODO_AUTO)) {
        modo_actual  = MODO_MANUAL;
        LCD_Clear();
    }

    // Manejar tiempo de advertencia en CONFIG (~2 s = 2000 llamadas)
    if (advertencia_activa) {
        advertencia_count++;
        if (advertencia_count >= 2000) {
            advertencia_activa = 0;
            advertencia_count  = 0;
        }
    }
}

// ???????????????????????????????????????????????????????????????????????????????
// ACTUALIZAR LCD
//   Se llama cada 500 ms (desde flag_timer) o en eventos de botón relevantes.
//   Adapta el contenido según el modo y vista activos.
// ???????????????????????????????????????????????????????????????????????????????
void actualizar_LCD(void) {
    char buf1[17], buf2[17];

    // ?? ALARMA activa: parpadeo "ALERTA TEMP!" ????????????????????????????????
    if (alarma_activa) {
        if (alarma_parpadeo) {
            LCD_Set_Cursor(1, 1);
            LCD_Write_String("  ALERTA TEMP!  ");
            LCD_Set_Cursor(2, 1);
            LCD_Write_String("   !! CRITICO!! ");
        } else {
            LCD_Clear();
        }
        return;  // No mostrar nada más mientras hay alarma
    }

    // ?? MODO CONFIGURACIÓN ????????????????????????????????????????????????????
    if (modo_actual == MODO_CONFIG) {
        if (advertencia_activa) {
            // Mensaje de advertencia de validación
            LCD_Set_Cursor(1, 1);
            LCD_Write_String("  VALOR INVALIDO");
            LCD_Set_Cursor(2, 1);
            LCD_Write_String("Von-Voff >= 5C! ");
            return;
        }
        // Línea 1: Von con indicador de selección
        LCD_Set_Cursor(1, 1);
        if (campo_edit == EDIT_VON)
            sprintf(buf1, "*Von:%2d  Voff:%2d", t_von, t_voff);
        else
            sprintf(buf1, " Von:%2d *Voff:%2d", t_von, t_voff);
        LCD_Write_String(buf1);
        // Línea 2: instrucciones
        LCD_Set_Cursor(2, 1);
        LCD_Write_String("UP/DN ajusta    ");
        return;
    }

    // ?? MODO MANUAL ???????????????????????????????????????????????????????????
    if (modo_actual == MODO_MANUAL) {
        LCD_Set_Cursor(1, 1);
        sprintf(buf1, "T:%.1fC  MANUAL  ", temperatura_actual);
        LCD_Write_String(buf1);
        LCD_Set_Cursor(2, 1);
        if (fan_on)
            LCD_Write_String("FAN:ON  U=ON D=OF");
        else
            LCD_Write_String("FAN:OFF U=ON D=OF");
        return;
    }

    // ?? MODO AUTOMÁTICO ???????????????????????????????????????????????????????
    switch (vista_actual) {

        case VISTA_NORMAL:
        default:
            // Línea 1: Voltaje y temperatura
            sprintf(buf1, "V:%.3fV T:%.1fC", voltaje_actual, temperatura_actual);
            LCD_Set_Cursor(1, 1);
            LCD_Write_String(buf1);
            // Línea 2: Estado FAN + modo
            LCD_Set_Cursor(2, 1);
            sprintf(buf2, "FAN:%s  AUTO    ", fan_on ? "ON " : "OFF");
            LCD_Write_String(buf2);
            break;

        case VISTA_MAX:
            LCD_Set_Cursor(1, 1);
            LCD_Write_String("  Temp. MAXIMA  ");
            LCD_Set_Cursor(2, 1);
            sprintf(buf2, "    %.1f C       ", temp_max > -999.0f ? temp_max : 0.0f);
            LCD_Write_String(buf2);
            break;

        case VISTA_MIN:
            LCD_Set_Cursor(1, 1);
            LCD_Write_String("  Temp. MINIMA  ");
            LCD_Set_Cursor(2, 1);
            sprintf(buf2, "    %.1f C       ", temp_min < 999.0f ? temp_min : 0.0f);
            LCD_Write_String(buf2);
            break;
    }
}

