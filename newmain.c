// ANIMACIÓN DE BARRA DE CARGA AL ARRANQUE
// ???????????????????????????????????????????????????????????????????????????????
void animacion_inicio(void) {
    LCD_Clear();
    LCD_Set_Cursor(1, 1);
    LCD_Write_String("  PT100 Sistema ");
    LCD_Set_Cursor(2, 1);
    for (uint8_t i = 0; i < 16; i++) {
        LCD_Set_Cursor(2, i + 1);
        LCD_Write_Char('#');
        __delay_ms(100);
    }
    __delay_ms(500);
    LCD_Clear();
}

// ???????????????????????????????????????????????????????????????????????????????
// MAIN
// ???????????????????????????????????????????????????????????????????????????????
void main(void) {

    // ?? Configuración de puertos ??????????????????????????????????????????????
    TRISA  = 0xFF;          // RA0/AN0 ? entrada analógica
    TRISB  = 0xFF;          // RB0..RB2 como entradas (botones con pull-up)
    TRISC  = 0xFF;          // RC3(SCL) / RC4(SDA) ? MSSP I2C
    TRISD  = 0x00;          // Puerto D completo como salida
    PORTD  = 0x00;          // Todo apagado al inicio

    FAN_TRIS   = 0;         // RD0 salida (ventilador)
    ALARM_TRIS = 0;         // RD1 salida (alarma)

    // Habilitar pull-ups internos en PORTB
    OPTION_REGbits.nRBPU = 0;  // 0 = pull-ups habilitados

    // ?? Configuración ADC ?????????????????????????????????????????????????????
    // ADFM=1 resultado justificado a la derecha
    // AN0 analógico, resto digitales, Vref = VDD/VSS
    ADCON1 = 0b10000000;
    // Fosc/8, canal AN0, ADC habilitado
    ADCON0 = 0b01000001;
    __delay_ms(10);

    // ?? Inicialización I2C + LCD ??????????????????????????????????????????????
    I2C_Master_Init();
    __delay_ms(50);
    LCD_Init(LCD_ADDR);
    __delay_ms(20);

    // ?? Animación de bienvenida ???????????????????????????????????????????????
    animacion_inicio();

    // ?? Cargar umbrales desde EEPROM ??????????????????????????????????????????
    cargar_umbrales_EEPROM();

    // ?? Configurar Timer0 para temporizaciones ????????????????????????????????
    configurar_timer0();

    // ?? Leer temperatura inicial para historial ???????????????????????????????
    {
        uint16_t adc_init = leer_ADC();
        float    volt_init = 0.0f;
        float    temp_init = convertir_temperatura(adc_init, &volt_init);
        temp_max = temp_init;
        temp_min = temp_init;
        voltaje_actual     = volt_init;
        temperatura_actual = temp_init;
    }

    // ?????????????????????????????????????????????????????????????????????????
    // BUCLE PRINCIPAL
    //   - leer_botones() se llama en cada iteración (~1 ms por __delay_ms(1))
    //   - Las tareas periódicas (ADC, LCD, alarma, histéresis) solo cuando
    //     flag_timer = 1 (cada ~500 ms, disparado por Timer0)
    // ?????????????????????????????????????????????????????????????????????????
    while (1) {

        // Lectura de botones: frecuente para buen anti-rebote y pulsación larga
        leer_botones();
        __delay_ms(1);      // Mínimo delay para dar ~1 ms entre lecturas de btn

        // Tareas periódicas cada 500 ms
        if (flag_timer) {
            flag_timer = 0;

            // 1) Leer ADC y convertir
            uint16_t adc_val = leer_ADC();
            temperatura_actual = convertir_temperatura(adc_val, &voltaje_actual);

            // 2) Actualizar historial de temperatura
            if (temperatura_actual > temp_max) temp_max = temperatura_actual;
            if (temperatura_actual < temp_min) temp_min = temperatura_actual;

            // 3) Control de histéresis del ventilador (solo en AUTO)
            control_histeresis();

            // 4) Control de alarma
            control_alarma();

            // 5) Actualizar LCD
            actualizar_LCD();
        }
    }
}